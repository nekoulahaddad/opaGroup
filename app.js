let currentLang = "en";

const updateText = (lang) => {
  const t = translations[lang] || translations.en;
  currentLang = lang;
  document.documentElement.lang = lang;
  document.documentElement.dir = t.dir;
  document.title = t.page_title;

  const meta = document.querySelector('meta[name="description"]');
  if (meta) meta.setAttribute('content', t.meta_description);
  const og = document.querySelector('meta[property="og:description"]');
  if (og) og.setAttribute('content', t.meta_description);

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) el.textContent = t[key];
  });

  document.querySelectorAll('[data-i18n-list]').forEach((el) => {
    const key = el.getAttribute('data-i18n-list');
    const items = t[key];
    if (!items) return;
    el.innerHTML = '';
    items.forEach((item) => {
      const li = document.createElement('li');
      li.textContent = item;
      el.appendChild(li);
    });
  });

  renderCards('services-core', t.services_core);
  renderCards('services-market', t.services_market);
  renderCards('services-extended', t.services_extended);
  renderOffices();
};

const renderCards = (containerId, items) => {
  const container = document.getElementById(containerId);
  if (!container || !items) return;
  container.innerHTML = '';
  items.forEach((item) => {
    const card = document.createElement('div');
    card.className = 'card';
    const title = document.createElement('h3');
    title.textContent = item.title;
    const text = document.createElement('p');
    text.textContent = item.text;
    card.appendChild(title);
    card.appendChild(text);
    container.appendChild(card);
  });
};

const renderOffices = () => {
  const container = document.getElementById('offices');
  if (!container) return;
  container.innerHTML = '';
  const names = officesI18n[currentLang] || officesI18n.en;
  offices.forEach((office, index) => {
    const card = document.createElement('div');
    card.className = 'card';
    const title = document.createElement('h3');
    title.textContent = names[index] || office.country;
    const address = document.createElement('p');
    address.textContent = office.address;
    const phones = document.createElement('p');
    phones.textContent = office.phones.join(' | ');
    card.appendChild(title);
    card.appendChild(address);
    card.appendChild(phones);
    container.appendChild(card);
  });
};

const initLangSwitch = () => {
  const select = document.getElementById('lang-select');
  const dropdown = document.getElementById('lang-dropdown');
  if (!select || !dropdown) return;

  const trigger = dropdown.querySelector('.lang-trigger');
  const menu = dropdown.querySelector('.lang-menu');
  const items = dropdown.querySelectorAll('[data-lang]');

  const setActive = (lang) => {
    items.forEach((item) => item.classList.toggle('active', item.getAttribute('data-lang') === lang));
    const active = dropdown.querySelector(`[data-lang=\"${lang}\"]`);
    if (active) {
      const name = active.querySelector('.lang-name')?.textContent || '';
      const triggerName = trigger.querySelector('.lang-name');
      if (triggerName) triggerName.textContent = name;
    }
  };

  const closeMenu = () => {
    dropdown.classList.remove('open');
    trigger.setAttribute('aria-expanded', 'false');
  };

  trigger.addEventListener('click', (e) => {
    e.stopPropagation();
    const isOpen = dropdown.classList.toggle('open');
    trigger.setAttribute('aria-expanded', isOpen ? 'true' : 'false');
  });

  items.forEach((item) => {
    item.addEventListener('click', () => {
      const lang = item.getAttribute('data-lang');
      select.value = lang;
      updateText(lang);
      setActive(lang);
      closeMenu();
    });
  });

  document.addEventListener('click', () => closeMenu());
  menu.addEventListener('click', (e) => e.stopPropagation());

  select.addEventListener('change', () => {
    updateText(select.value);
    setActive(select.value);
  });

  setActive(select.value);
};

const initReveal = () => {
  const items = document.querySelectorAll('.section, .card, .team-card, .hero-card, .hero-visual, .media-card, .presence-visual');
  items.forEach((el) => el.classList.add('reveal'));

  if (!('IntersectionObserver' in window)) {
    items.forEach((el) => el.classList.add('is-visible'));
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add('is-visible');
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.12 }
  );

  items.forEach((el) => observer.observe(el));
};

const initCounters = () => {
  const counters = document.querySelectorAll('.figure-number[data-count]');
  if (!counters.length) return;

  const run = (el) => {
    const target = parseInt(el.getAttribute('data-count') || '0', 10);
    let current = 0;
    const step = Math.max(1, Math.floor(target / 40));
    const tick = () => {
      current += step;
      if (current >= target) {
        el.textContent = String(target);
        return;
      }
      el.textContent = String(current);
      requestAnimationFrame(tick);
    };
    tick();
  };

  if (!('IntersectionObserver' in window)) {
    counters.forEach(run);
    return;
  }

  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          run(entry.target);
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.4 }
  );

  counters.forEach((el) => observer.observe(el));
};

